public class ForLoop5 {
    public static void main(String[] args) {
//print the multiplication table of 5

        for (int i = 0; i <=10 ; i++) {
            System.out.println("5 * " + i + " = " + (5 * i));
        }
        for (int i = 0; i <=10 ; i++) {
            System.out.println("3 * "+i+" = "+(3*i));



        }
    }
}
