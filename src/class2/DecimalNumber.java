package class2;

public class DecimalNumber {
    public static void main(String[] args) {
        /*
        if we want to store decimal numbers in computer's memory using java we can use following types of boxes
        float
        decimal
         */
        float myNumberBox=10.5f;
        float mySecondBox=5.5f;
        double largeDecimalBox=1345454455.12121;
        System.out.println(myNumberBox);
        /*
        If we want to store characters (#,$,o,s,4) we can use char datatype
         */
        char letter='s';
        char gender='M';
        char symbol='#';
        char numberChar='1';
        /*
        String data type belongs to a different category called non-primitives that's why for String rules are a little
        different. For example, S of String is upper case and has a different color
         */
        String name="Sam";

        boolean hungry=true; // true means yes
        boolean sleepy=false; // false means no


    }
}
